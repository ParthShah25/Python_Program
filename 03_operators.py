#Arithmetic Operators
num1 = 45
num2 = 63

print("The value of Num1 + Num2 is: ", num1 + num2)
print("The value of Num1 - Num2 is: ", num1 - num2)
print("The value of Num1 / Num2 is: ", num1 / num2)
print("The value of Num1 * Num2 is: ", num1 * num2)
print("The value of Num1 % Num2 is: ", num1 % num2)

#Assignment Operators
num1 += 12
print(num1)
num1 -= 12
print(num1)
num1 *= 12
print(num1)
num1 /= 12
print(num1)

#Comparison Operator
print(num1>num2)
print(num1<num2)
print(num1<=num2)
print(num1>=num2)
print(num1==num2)
print(num1!=num2)

#Logical Operator
bool1 = False
bool2 = True
print("The Value of bool1 and bool2 is: ",(bool1 and bool2))
print("The Value of bool1 or bool2 is: ",(bool1 or bool2))
print("The Value of not bool2 is: ",(not bool2))
